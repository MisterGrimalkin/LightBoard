package net.amarantha.lightboard.scene;

public class XMLSceneException extends Exception {

    public XMLSceneException() {
    }

    public XMLSceneException(String message) {
        super(message);
    }

    public XMLSceneException(String message, Throwable cause) {
        super(message, cause);
    }

    public XMLSceneException(Throwable cause) {
        super(cause);
    }

    public XMLSceneException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
